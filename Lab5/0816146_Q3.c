#include <stdio.h>
	
#define a0 *(*a+a)
#define a1 *(*a+a+1)

int $ = '$';
int a[] = {'*'^'?'};

int main() {
	printf("Input: ");
	while (0 <-- *a)
		scanf("%d", *a+a);

	while ($ --> 0)
		for (*a=1; *a<20; ++*a)
			if (a0 < a1)
				a0 ^= a1 ^= a0 ^= a1;

	printf("Output: ");
	for (*a=__LINE__; *a;)
		printf("%d%c", a0, ' '^(--*a ? 0 : '*'));

	return *a;
}
